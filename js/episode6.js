// Déclaration des variables
let nombre1;
let nombre2;
let nombre3;

// Demande des nombres à l'utilisateur
nombre1 = prompt("Saisissez le premier nombre :");
nombre2 = prompt("Saisissez le deuxième nombre :");
nombre3 = prompt("Saisissez le troisième nombre :");

// Calcul de la moyenne
let moyenne = (nombre1 + nombre2 + nombre3) / 3;

// Affichage de l'alerte
alert("La moyenne des trois nombres est de " + moyenne);
